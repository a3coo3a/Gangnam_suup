package day05;

public class BreakEx01 {
	public static void main(String[] args) {

		/*
		 * 무한 반복문 - 반복문의 횟수를 정확히 모를때 주로 사용 
		 * 자바에서는 while(true) 문장을 많이 사용
		 * 
		 */
		int i = 1;
		while (true) {
			if (i == 15) {
				break;
			}
			System.out.println(i);
			i++;
		}

		
		
		
		
		
		
		
		
		
	}
}
